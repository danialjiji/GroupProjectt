package com.example.groupproject;

public class Friend {
    String name, phone, email, age, gender, dob;

    public Friend(String name, String phone, String email, String age, String gender, String dob) {
        this.name = name;
        this.phone = phone;
        this.email = email;
        this.age = age;
        this.gender = gender;
        this.dob = dob;
    }
}